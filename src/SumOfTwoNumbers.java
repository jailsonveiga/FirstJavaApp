// Return the Sum of Two Numbers

public class SumOfTwoNumbers {

    public static void main(String[] args) {
        System.out.println("return the sum of two numbers: (5, 11) equal " + sum(5,11));

        System.out.println("return the sum of two numbers: (15, 11) equal " + sum(15,11));
    }
    public static int sum(int a, int b) {
        return a + b;
    }
}
