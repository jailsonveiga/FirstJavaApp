// Return the Next Number from the Integer Passed

public class nextnumber {
    public static void main(String[] args) {
        System.out.println("Return the Number after 6? " +  addition(6));
        System.out.println("Return the Number after 43? " +  addition(43));
        System.out.println("Return the Number after 0? " +  addition(0));
    }
    public static int addition(int num) {
        return num + 1;
    }
}
